#! /bin/bash

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Various echo helper functions
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

echo_pass{
	echo "\033[32m[✔]\033[0m $1"
}

echo_fail{
	echo "\033[31m[✘]\033[0m $1"
}

echo_red{
	echo "\033[31m$1\033[0m"
}

echo_green{
	echo "\033[32m$1\033[0m"
}
